#include <iostream>
#include <vector>
#include "../../ctor-dtor/Cube.h"

using namespace std;

int main() {
    
    int a[5] = {1,2,3,4,5};
    Cube Cubes[3] = { Cube(90000), Cube(10), Cube(18) };
    vector<Cube>v_cubes;
    
    // Using poiter arithmetic, calculate the offset
    cout << "sizeof(int) = " <<  sizeof(int) << endl;
    int offset = (long)&a[2] - (long)&a[0];
    cout << "offset = " << offset << endl;

    cout << "sizeof(Cube) = " << sizeof(Cube) << endl;
    int cubeOffset = (long)&Cubes[2] - (long)&Cubes[0];
    cout << "Cube Offset = " << cubeOffset << endl;

    v_cubes.push_back(Cube(500));
    v_cubes.push_back(Cube(600));
    v_cubes.push_back(Cube(700));

    // Capacity 
    cout << "=========================================" << endl;
    cout << "Initial capacity = " << v_cubes.capacity() << endl;
    cout << "Initial size = " << v_cubes.size() << endl; 
    v_cubes.push_back(Cube(1000)); 
    v_cubes.push_back(Cube(1001)); 
    cout << "Final size = " << v_cubes.size() << endl; 
    cout << "Final capacity = " << v_cubes.capacity() << endl;
    cout << "=========================================" << endl;

    return 0;
}